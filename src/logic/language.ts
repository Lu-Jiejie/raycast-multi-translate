type FromArray<T extends readonly string[]> = Partial<Record<T[number], string>>
export type LanguageMap = FromArray<typeof languageCodeArr>
export type LanguageCode = keyof LanguageMap

export function getFromCode(
  from: LanguageCode,
  languageMap: LanguageMap,
): string {
  return languageMap[from] !== undefined ? languageMap[from] : from
}

export function getToCode(
  to: LanguageCode,
  languageMap: LanguageMap,
): string {
  return languageMap[to] !== undefined ? languageMap[to] : to
}
